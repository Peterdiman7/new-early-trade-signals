<script setup lang="ts">
import { onMounted, onBeforeUnmount, nextTick, ref } from 'vue'

const phone = ref('')

declare global {
	interface Window {
		jQuery: any;
		$: any;
	}
}

let carouselInstances: any[] = []

onMounted(async () => {
	await nextTick()

	// Add delay to ensure jQuery plugins are loaded
	setTimeout(() => {
		initializeComponents()
	}, 150)
})

onBeforeUnmount(() => {
	// Cleanup
	carouselInstances.forEach(instance => {
		try {
			if (instance && typeof instance.trigger === 'function') {
				instance.trigger('destroy.owl.carousel')
			}
		} catch (e) {
			console.warn('Carousel cleanup error:', e)
		}
	})
	carouselInstances = []
})

const activateSubscription = async () => {
	try {
		const response = await fetch("https://back.early-trade-signals.com/subscription/activate", {
			method: "POST",
			credentials: "include",
			headers: {
				"Content-Type": "application/json"
			}
		})
		const text = await response.text()
		const data = JSON.parse(text)
	} catch (err) {
		console.error("Activation error:", err);
	}
}

function initializeComponents() {
	const $ = window.$ || window.jQuery

	if (!$) {
		console.error('jQuery not available')
		return
	}

	// Initialize all carousels
	initCarousel('.banner-carousel', {
		items: 1,
		loop: true,
		autoplay: true,
		autoplayTimeout: 5000,
		nav: false,
		dots: false,
		animateOut: 'fadeOut',
		animateIn: 'fadeIn'
	})

	initCarousel('.five-item-carousel', {
		loop: true,
		margin: 30,
		nav: false,
		dots: true,
		autoplay: true,
		responsive: {
			0: { items: 1 },
			600: { items: 2 },
			768: { items: 3 },
			992: { items: 4 },
			1200: { items: 5 }
		}
	})

	// Initialize other plugins
	initAccordion($)
	initTabs($)
	initOdometer($)
	initNiceSelect($)
	initScrollToTop($)
}

function initCarousel(selector: string, options: any) {
	const $ = window.$
	const element = $(selector)

	if (element.length && typeof $.fn.owlCarousel === 'function') {
		element.each(function () {
			const instance = $(this).owlCarousel(options)
			carouselInstances.push(instance)
		})
	}
}

function initAccordion($: any) {
	if ($('.accordion-box').length) {
		$('.accordion-box').on('click', '.acc-btn', function () {
			const $btn = $(this)
			const $accordion = $btn.closest('.accordion-box')

			if (!$btn.hasClass('active')) {
				$accordion.find('.acc-btn').removeClass('active')
				$accordion.find('.accordion').removeClass('active-block')
				$accordion.find('.acc-content').slideUp(300)
			}

			$btn.toggleClass('active')
			$btn.closest('.accordion').toggleClass('active-block')
			$btn.next('.acc-content').slideToggle(300)
		})
	}
}

function initTabs($: any) {
	$('.p-tab-btn').on('click', function (e: Event) {
		e.preventDefault()
		const target = $(this).attr('data-tab')

		$('.p-tab-btn').removeClass('active-btn')
		$(this).addClass('active-btn')

		$('.p-tab').removeClass('active-tab')
		$(target).addClass('active-tab')
	})
}

function initOdometer($: any) {
	if ($('.odometer').length && $.fn.appear) {
		$('.odometer').each(function () {
			$(this).appear(function () {
				const count = $(this).attr('data-count')
				$(this).html(count)
			})
		})
	}
}

function initNiceSelect($: any) {
	if ($.fn.niceSelect && $('select:not(.ignore)').length) {
		$('select:not(.ignore)').niceSelect()
	}
}

function initScrollToTop($: any) {
	$(window).on('scroll', function () {
		if ($(this).scrollTop() > 200) {
			$('.scroll-to-top').fadeIn()
		} else {
			$('.scroll-to-top').fadeOut()
		}
	})

	$('.scroll-to-top').on('click', function () {
		$('html, body').animate({ scrollTop: 0 }, 600)
		return false
	})
}
